class MainActivity : ComponentActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        ActivityCompat.requestPermissions(
            this,
            arrayOf(
                Manifest.permission.ACCESS_COARSE_LOCATION,
                Manifest.permission.ACCESS_FINE_LOCATION,
                Manifest.permission.POST_NOTIFICATIONS
            ),
            100
        )

        setContent {
            val viewModel: LocationViewModel = viewModel()
            val locations by viewModel.locations.observeAsState(emptyList())

            TrackerScreen(
                startTracking = {
                    Intent(applicationContext, LocationTrackerService::class.java).also {
                        it.action = LocationTrackerService.Action.START.name
                        startService(it)
                    }
                },
                stopTracking = {
                    Intent(applicationContext, LocationTrackerService::class.java).also {
                        it.action = LocationTrackerService.Action.STOP.name
                        startService(it)
                    }
                },
                clearDatabase = { viewModel.clearLocations() },
                locations = locations
            )
        }
    }
}
